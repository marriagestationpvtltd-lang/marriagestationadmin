    if (_selectedDiet.isNotEmpty && !_hasAnySelected(_selectedDiet)) {
      expectations.add('Diet preference: ${_selectedDiet.join(", ")}.');
    }

    // Physical preferences
    if (_selectedComplexion.isNotEmpty && !_hasAnySelected(_selectedComplexion)) {
      expectations.add('Complexion preference: ${_selectedComplexion.join(", ")}.');
    }

    if (_selectedBodyType.isNotEmpty && !_hasAnySelected(_selectedBodyType)) {
      expectations.add('Body type preference: ${_selectedBodyType.join(", ")}.');
    }

