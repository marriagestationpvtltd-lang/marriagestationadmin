      _autoGeneratedExpectations.clear();
      _autoGeneratedExpectations.addAll(expectations);

      // Update the expectations controller with auto-generated text
      if (_autoGeneratedExpectations.isNotEmpty) {
        _otherExpectationsController.text = _autoGeneratedExpectations.join('\n');
      }
    });
  }

