  // Validation methods
  bool _validateRequiredList(List<String> list, String fieldName) {
    if (list.isEmpty) {
      _errors[fieldName] = 'Please select at least one option';
      return false;
    }
    _errors[fieldName] = '';
    return true;
  }

  bool _validateRequired(String? value, String fieldName) {
    if (value == null || value.isEmpty) {
      _errors[fieldName] = 'This field is required';
      return false;
    }
    _errors[fieldName] = '';
    return true;
  }

  bool _validateForm() {
    bool isValid = true;

    // Clear all errors
    _errors.forEach((key, value) {
      _errors[key] = '';
    });

    // Basic Preferences validation
    if (!_validateRequiredList(_selectedMaritalStatus, 'maritalStatus')) {
      isValid = false;
    }

    // Profile with children validation (only required if not single/any)
    if (!_hasAnySelected(_selectedMaritalStatus) && !_selectedMaritalStatus.contains('Single')) {
      if (!_validateRequired(_selectedProfileWithChildren, 'profileWithChildren')) {
        isValid = false;
      }
    } else {
      // Default to "No child" for single or any
      _selectedProfileWithChildren = 'No child';
    }

    if (!_validateRequiredList(_selectedFamilyType, 'familyType')) {
      isValid = false;
    }

    // Community Details validation
    if (!_validateRequiredList(_selectedReligion, 'religion')) {
      isValid = false;
    }
    if (!_validateRequiredList(_selectedCast, 'cast')) {
      isValid = false;
    }
    if (!_validateRequiredList(_selectedsubCast, 'subCast')) {
      isValid = false;
    }
    if (!_validateRequiredList(_selectedMotherTongue, 'motherTongue')) {
      isValid = false;
    }

    // Astrological Details validation
    if (!_validateRequired(_selectedHoroscopeBelief, 'horoscopeBelief')) {
      isValid = false;
    }
    if (!_validateRequired(_selectedManglik, 'manglik')) {
      isValid = false;
    }

    // Location Details validation
    if (!_validateRequiredList(_selectedCountry, 'country')) {
      isValid = false;
    }
    if (!_hasAnySelected(_selectedCountry) && !_validateRequiredList(_selectedState, 'state')) {
      isValid = false;
    }
    if (!_hasAnySelected(_selectedState) && !_validateRequiredList(_selectedCity, 'city')) {
      isValid = false;
    }

    // Education Details validation
    if (!_validateRequiredList(_selectedQualification, 'qualification')) {
      isValid = false;
    }
    if (!_validateRequiredList(_selectedEducationMedium, 'educationMedium')) {
      isValid = false;
    }

    // Profession Details validation
    if (!_validateRequiredList(_selectedProfession, 'profession')) {
      isValid = false;
    }
    if (!_validateRequiredList(_selectedWorkingWith, 'workingWith')) {
      isValid = false;
    }
    if (!_validateRequiredList(_selectedAnnualIncome, 'annualIncome')) {
      isValid = false;
    }

    // Other Details validation
    if (!_validateRequiredList(_selectedDiet, 'diet')) {
      isValid = false;
    }
    if (!_validateRequired(_selectedSmokeAcceptance, 'smokeAcceptance')) {
      isValid = false;
    }
    if (!_validateRequired(_selectedAlcoholAcceptance, 'alcoholAcceptance')) {
      isValid = false;
    }
    if (!_validateRequired(_selectedDisabilityAcceptance, 'disabilityAcceptance')) {
      isValid = false;
    }
    if (!_validateRequiredList(_selectedComplexion, 'complexion')) {
      isValid = false;
    }
    if (!_validateRequiredList(_selectedBodyType, 'bodyType')) {
      isValid = false;
    }

    setState(() {});
    return isValid;
  }

  // Handler methods
  void _handleCountryChange(List<String> selectedItems) {
    setState(() {
      _selectedCountry = selectedItems;
      _errors['country'] = '';

      // Reset states and cities when countries change
      _selectedState = ['Any'];
      _selectedCity = ['Any'];
      _allStates.clear();
      _allCities.clear();
    });

    // Load states for selected countries
    _loadStatesForCountries(_getCountryIds(selectedItems));
    _updateAutoGeneratedExpectations();
  }

  void _handleStateChange(List<String> selectedItems) {
    setState(() {
      _selectedState = selectedItems;
      _errors['state'] = '';

      // Reset cities when states change
      _selectedCity = ['Any'];
      _allCities.clear();
    });

    // Load cities for selected states
    _loadCitiesForStates(_getStateIds(selectedItems));
    _updateAutoGeneratedExpectations();
  }

  void _handleCityChange(List<String> selectedItems) {
    setState(() {
      _selectedCity = selectedItems;
      _errors['city'] = '';
    });

    _updateAutoGeneratedExpectations();
  }

  void _handleMultiSelectChange(List<String> selectedItems, String fieldName, Function(List<String>) updateFunction) {
    // If "Any" is selected, clear all other selections and only keep "Any"
    if (selectedItems.contains('Any')) {
      selectedItems = ['Any'];
    }
    // If something else is selected and "Any" was previously selected, remove "Any"
    else if (selectedItems.isNotEmpty) {
      selectedItems.remove('Any');
    }

    updateFunction(selectedItems);
    _updateAutoGeneratedExpectations();
  }

  void _handleSingleSelectChange(String? value, String fieldName, Function(String?) updateFunction) {
    updateFunction(value);
    _updateAutoGeneratedExpectations();
  }

