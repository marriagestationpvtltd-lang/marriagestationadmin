  Future<void> _loadSavedPreferences() async {
    try {
      final prefs = await SharedPreferences.getInstance();
      final userDataString = prefs.getString('user_data');

      if (userDataString == null) {
        return;
      }

      final userData = jsonDecode(userDataString);
      final userId = userData["id"]?.toString() ?? '';

      if (userId.isEmpty) {
        return;
      }

      setState(() {
        isLoading = true;
      });

      final response = await http.get(
        Uri.parse("https://digitallami.com/Api2/get_partner_preferences.php?userid=$userId"),
      ).timeout(const Duration(seconds: 30));

      if (response.statusCode == 200) {
        final result = jsonDecode(response.body);

        if (result['status'] == 'success' && result['data'] != null) {
          final data = result['data'];

          setState(() {
            // Age Range
            _minAge = double.parse(data['minage'] ?? '22');
            _maxAge = double.parse(data['maxage'] ?? '60');

            // Height Range
            _minHeight = double.parse(data['minheight'] ?? '121');
            _maxHeight = double.parse(data['maxheight'] ?? '215');

            // Basic Preferences
            _selectedMaritalStatus = List<String>.from(data['maritalstatus'] ?? ['Any']);
            _selectedProfileWithChildren = data['profilewithchild'];
            _selectedFamilyType = List<String>.from(data['familytype'] ?? ['Any']);

            // Community Details
            _selectedReligion = List<String>.from(data['religion'] ?? ['Any']);
            _selectedCast = List<String>.from(data['caste'] ?? ['Any']);
            _selectedsubCast = List<String>.from(data['subcaste'] ?? ['Any']);
            _selectedMotherTongue = List<String>.from(data['mothertoungue'] ?? ['Any']);

            // Astrological Details
            _selectedHoroscopeBelief = data['herscopeblief'];
            _selectedManglik = data['manglik'];

            // Location Details
            _selectedCountry = List<String>.from(data['country'] ?? ['Any']);
            _selectedState = List<String>.from(data['state'] ?? ['Any']);
            _selectedCity = List<String>.from(data['city'] ?? ['Any']);

            // Education Details
            _selectedQualification = List<String>.from(data['qualification'] ?? ['Any']);
            _selectedEducationMedium = List<String>.from(data['educationmedium'] ?? ['Any']);

            // Profession Details
            _selectedProfession = List<String>.from(data['proffession'] ?? ['Any']);
            _selectedWorkingWith = List<String>.from(data['workingwith'] ?? ['Any']);
            _selectedAnnualIncome = List<String>.from(data['annualincome'] ?? ['Any']);

            // Other Details
            _selectedDiet = List<String>.from(data['diet'] ?? ['Any']);
            _selectedSmokeAcceptance = data['smokeaccept'];
            _selectedAlcoholAcceptance = data['drinkaccept'];
            _selectedDisabilityAcceptance = data['disabilityaccept'];
            _selectedComplexion = List<String>.from(data['complexion'] ?? ['Any']);
            _selectedBodyType = List<String>.from(data['bodytype'] ?? ['Any']);

            // Other Expectations
            _otherExpectationsController.text = data['otherexpectation'] ?? '';
          });

          // Load dependent data based on saved selections
          if (_selectedCountry.isNotEmpty && !_hasAnySelected(_selectedCountry)) {
            await _loadStatesForCountries(_getCountryIds(_selectedCountry));
          }

          if (_selectedState.isNotEmpty && !_hasAnySelected(_selectedState)) {
            await _loadCitiesForStates(_getStateIds(_selectedState));
          }

          // Regenerate expectations based on loaded data
          _updateAutoGeneratedExpectations();
        }
      }
    } catch (e) {
      print("Error loading saved preferences: $e");
    } finally {
      setState(() {
        isLoading = false;
      });
    }
  }

