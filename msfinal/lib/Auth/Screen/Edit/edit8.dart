  // API Service Methods
  Future<void> _loadCountries() async {
    setState(() => _loadingCountries = true);
    try {
      final response = await http.get(
        Uri.parse("https://digitallami.com/Api3/countries.php"),
      ).timeout(const Duration(seconds: 30));

      if (response.statusCode == 200) {
        final data = jsonDecode(response.body);
        if (data['status'] == 'success') {
          setState(() {
            _countries = (data['data'] as List)
                .map((item) => Country(id: item['id'].toString(), name: item['name']))
                .toList();
            // Add "Any" option at the beginning
            _countries.insert(0, Country(id: '0', name: 'Any'));
          });
        }
      } else {
        _showError("Failed to load countries. Please try again.");
      }
    } on TimeoutException {
      _showError("Timeout loading countries. Please check your connection.");
    } catch (e) {
      print("Error loading countries: $e");
      _showError("Error loading countries. Please try again.");
    } finally {
      setState(() => _loadingCountries = false);
    }
  }

  Future<void> _loadStatesForCountries(List<String> countryIds) async {
    if (countryIds.isEmpty || countryIds.contains('Any')) {
      setState(() => _allStates.clear());
      return;
    }

    setState(() => _loadingStates = true);
    final List<StateModel> loadedStates = [];

    try {
      for (final countryId in countryIds) {
        if (countryId == '0') continue; // Skip "Any"

        final response = await http.get(
          Uri.parse("https://digitallami.com/Api3/states.php?country_id=$countryId"),
        ).timeout(const Duration(seconds: 30));

        if (response.statusCode == 200) {
          final data = jsonDecode(response.body);
          if (data['status'] == 'success') {
            final states = (data['data'] as List)
                .map((item) => StateModel(
              id: item['id'],
              name: item['name'],
              countryId: countryId,
            ))
                .toList();
            loadedStates.addAll(states);
          }
        }
      }

      setState(() {
        _allStates = loadedStates;
        // Add "Any" option if we have states
        if (_allStates.isNotEmpty) {
          _allStates.insert(0, StateModel(id: 0, name: 'Any', countryId: '0'));
        }
      });
    } on TimeoutException {
      _showError("Timeout loading states. Please try again.");
    } catch (e) {
      print("Error loading states: $e");
      _showError("Error loading states. Please try again.");
    } finally {
      setState(() => _loadingStates = false);
    }
  }

  Future<void> _loadCitiesForStates(List<String> stateIds) async {
    if (stateIds.isEmpty || stateIds.contains('Any')) {
      setState(() => _allCities.clear());
      return;
    }

    setState(() => _loadingCities = true);
    final List<City> loadedCities = [];

    try {
      for (final stateId in stateIds) {
        if (stateId == 'Any' || stateId == '0') continue;

        final response = await http.get(
          Uri.parse("https://digitallami.com/Api3/cities.php?state_id=$stateId"),
        ).timeout(const Duration(seconds: 30));

        if (response.statusCode == 200) {
          final data = jsonDecode(response.body);
          if (data['status'] == 'success') {
            final cities = (data['data'] as List)
                .map((item) => City(
              id: item['id'],
              name: item['name'],
              stateId: int.parse(stateId),
            ))
                .toList();
            loadedCities.addAll(cities);
          }
        }
      }

      setState(() {
        _allCities = loadedCities;
        // Add "Any" option if we have cities
        if (_allCities.isNotEmpty) {
          _allCities.insert(0, City(id: 0, name: 'Any', stateId: 0));
        }
      });
    } on TimeoutException {
      _showError("Timeout loading cities. Please try again.");
    } catch (e) {
      print("Error loading cities: $e");
      _showError("Error loading cities. Please try again.");
    } finally {
      setState(() => _loadingCities = false);
    }
  }

  // Helper methods
  bool _hasAnySelected(List<String> list) {
    return list.contains('Any');
  }

  List<String> _getCountryIds(List<String> selectedNames) {
    if (selectedNames.contains('Any')) return ['0'];

    return selectedNames.map((name) {
      final country = _countries.firstWhere((c) => c.name == name);
      return country.id;
    }).toList();
  }

  List<String> _getStateIds(List<String> selectedNames) {
    if (selectedNames.contains('Any')) return ['0'];

    return selectedNames.map((name) {
      final state = _allStates.firstWhere((s) => s.name == name);
      return state.id.toString();
    }).toList();
  }

  List<String> _getCityIds(List<String> selectedNames) {
    if (selectedNames.contains('Any')) return ['0'];

    return selectedNames.map((name) {
      final city = _allCities.firstWhere((c) => c.name == name);
      return city.id.toString();
    }).toList();
  }

